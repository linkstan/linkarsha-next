export function getBentoConfig({

block,
index,
isMobile

}){

const type =
block?.data_json?.type;

const size =
block?.data_json?.size;

const variant =
block?.data_json?.variant;


/* ================================================= */
/* MOBILE */
/* ================================================= */

if(isMobile){

return{

colSpan:1,
rowSpan:1

};

}


/* ================================================= */
/* HERO */
/* ================================================= */

if(size === "hero"){

return{

colSpan:2,
rowSpan:2

};

}


/* ================================================= */
/* FEATURE */
/* ================================================= */

if(size === "feature"){

return{

colSpan:2,
rowSpan:1

};

}


/* ================================================= */
/* VIDEO */
/* ================================================= */

if(type === "video"){

return{

colSpan:2,
rowSpan:1

};

}


/* ================================================= */
/* GALLERY */
/* ================================================= */

if(type === "gallery"){

return{

colSpan:2,
rowSpan:2

};

}


/* ================================================= */
/* PRICING */
/* ================================================= */

if(type === "pricing"){

return{

colSpan:2,
rowSpan:1

};

}


/* ================================================= */
/* SPOTLIGHT */
/* ================================================= */

if(
variant === "spotlight"
){

return{

colSpan:2,
rowSpan:2

};

}


/* ================================================= */
/* DEFAULT */
/* ================================================= */

return{

colSpan:1,
rowSpan:1

};

}
